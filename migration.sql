-- Таблица для хранения Telegram сообщений
CREATE TABLE IF NOT EXISTS telegram_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message_id BIGINT NOT NULL,
    chat_id BIGINT NOT NULL,
    chat_title TEXT,
    sender_id BIGINT,
    sender_name TEXT NOT NULL,
    sender_username TEXT,
    text TEXT NOT NULL,
    date TIMESTAMP WITH TIME ZONE NOT NULL,
    ai_analysis JSONB,
    processed BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    
    CONSTRAINT unique_message UNIQUE (message_id, chat_id)
);

-- Индексы для быстрого поиска
CREATE INDEX IF NOT EXISTS idx_telegram_messages_chat_id ON telegram_messages(chat_id);
CREATE INDEX IF NOT EXISTS idx_telegram_messages_sender_id ON telegram_messages(sender_id);
CREATE INDEX IF NOT EXISTS idx_telegram_messages_date ON telegram_messages(date DESC);
CREATE INDEX IF NOT EXISTS idx_telegram_messages_ai_analysis ON telegram_messages USING GIN (ai_analysis);

-- RLS политики
ALTER TABLE telegram_messages ENABLE ROW LEVEL SECURITY;

-- Разрешить чтение всем авторизованным
CREATE POLICY "Allow read access for authenticated users"
    ON telegram_messages FOR SELECT
    TO authenticated
    USING (true);

-- Разрешить вставку всем авторизованным
CREATE POLICY "Allow insert for authenticated users"
    ON telegram_messages FOR INSERT
    TO authenticated
    WITH CHECK (true);


-- Вьюха для последних сообщений по клиентам
CREATE OR REPLACE VIEW latest_client_messages AS
SELECT DISTINCT ON (sender_id)
    sender_id,
    sender_name,
    sender_username,
    text,
    date,
    ai_analysis
FROM telegram_messages
WHERE sender_username IS NOT NULL
ORDER BY sender_id, date DESC;
